#include "STDexecDTS.h"

/*
 * allocstr() - Return pointer to allocated string generated by providing 
 * printf()-like arguments. Returns NULL on error.
 */

char *allocstr(const char *format, ...)
{
	va_list ap;
	char *p;
	size_t size = BUFSIZ;

	assert(format);

	p = malloc(size);
	if (!p) {
		myerror("%s(): Cannot allocate %u bytes", /* gncov */
		        __func__, size);
		return NULL; /* gncov */
	}

	while (1) {
		int i;
		char *p2;

		va_start(ap, format);
		i = vsnprintf(p, size, format, ap);
		va_end(ap);

		if (i < 0) {
			myerror("%s(): Cannot write %s bytes" /* gncov */
			        " to memory buffer", __func__, size);
			return NULL; /* gncov */
		}

		if ((size_t)i < size)
			return p;

		size += BUFSIZ;

		p2 = realloc(p, size);
		if (!p2) {
			myerror("%s(): Cannot reallocate %u bytes", /* gncov */
			        __func__, size);
			free(p); /* gncov */
			return NULL; /* gncov */
		}
		p = p2;
	}
}

