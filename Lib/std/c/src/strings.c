#include "STDexecDTS.h"

/*
 * allocstr_va() - Returns pointer to allocated string generated by providing 
 * printf()-like format and va_list. Returns NULL on error.
 */

char *allocstr_va(const char *format, va_list ap)
{
	char *p;
	size_t size = BUFSIZ;
	int needed;
	va_list ap_copy;

	assert(format);
	if (!format) {
		myerror("%s(): format is NULL", __func__); /* gncov */
		return NULL; /* gncov */
	}

	p = malloc(size);
	if (!p) {
		myerror("%s():%d: Cannot allocate %zu bytes", /* gncov */
		        __func__, __LINE__, size);
		return NULL; /* gncov */
	}

	va_copy(ap_copy, ap);
	needed = vsnprintf(p, size, format, ap);
	if (needed < 0) {
		failed("vsnprintf()"); /* gncov */
		free(p); /* gncov */
		va_end(ap_copy); /* gncov */
		return NULL; /* gncov */
	}
	if ((size_t)needed >= size) {
		free(p);
		size = (size_t)needed + 1;
		p = malloc(size);
		if (!p) {
			myerror("%s():%d: Cannot allocate %zu" /* gncov */
			        " bytes", __func__, __LINE__, size);
			va_end(ap_copy); /* gncov */
			return NULL; /* gncov */
		}
		needed = vsnprintf(p, size, format, ap_copy);
		if (needed < 0) {
			failed("vsnprintf()"); /* gncov */
			free(p); /* gncov */
			va_end(ap_copy); /* gncov */
			return NULL; /* gncov */
		}
	}
	va_end(ap_copy);

	return p;
}

/*
 * allocstr() - Returns pointer to allocated string generated by providing 
 * printf()-like arguments. Returns NULL on error.
 */

char *allocstr(const char *format, ...)
{
	va_list ap;
	char *retval;

	va_start(ap, format);
	retval = allocstr_va(format, ap);
	va_end(ap);
	if (!retval)
		failed("allocstr_va()"); /* gncov */

	return retval;
}

